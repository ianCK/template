template <typename T> inline T BASIC_OPERATION_MIN(T a, T b) {return min(a, b);}
template <typename T> inline T BASIC_OPERATION_MAX(T a, T b) {return max(a, b);}
template <typename T> inline T BASIC_OPERATION_ADD(T a, T b) {return a + b;}
template <typename T> inline T BASIC_OPERATION_MUL(T a, T b) {return a * b;}
template <typename T> inline T BASIC_OPERATION_XOR(T a, T b) {return a ^ b;}
template <typename T> inline T BASIC_OPERATION_AND(T a, T b) {return a & b;}
template <typename T> inline T BASIC_OPERATION_OR (T a, T b) {return a | b;}
